function genVolumes(total: number, pct: number): number[] {
  const count = Math.round(total * pct / 100)
  const owned: number[] = []
  for (let i = 1; i <= total && owned.length < count; i++) {
    if (i <= Math.ceil(count * 1.1)) owned.push(i)
  }
  return owned.slice(0, count)
}

